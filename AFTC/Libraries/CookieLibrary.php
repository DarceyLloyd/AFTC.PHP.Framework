namespace AFTC\Framework\App\Libraries;

use AFTC\Framework\App\Libraries\LogLibrary;
use AFTC\Framework\Config;
use AFTC\Framework\Core\Helper;
use AFTC\Framework\App\Libraries\EncryptionLibrary;

class CookieLibrary extends Helper
{
	// - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
	private $encryption;
	// - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -


	// - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
	public function __construct()
	{
		$this->encryption = new EncryptionLibrary();
	}
	// - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -


	// - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
	public function set($key,$value)
	{
		$EncryptedKey = base64_encode($this->encryption->ecbEncrypt($key));
		$EncryptedValue = $this->encryption->encrypt($value);
		//trace($EncryptedKey);
		//trace($EncryptedValue);
		//setcookie( $key, $EncryptedValue, strtotime( '+0.5 minutes' ) );
		setcookie( $key, $EncryptedValue, time() + (Config::$cookie_expiration_time * 60) );
	}
	// - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -


	// - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
	public function get($key)
	{
		$DecryptedValue = "";

		if (isset($_COOKIE[$key])){
			$EncryptedValue = $_COOKIE[$key];
			$DecryptedValue = $this->encryption->decrypt($EncryptedValue);
		}

		return $DecryptedValue;
	}
	// - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -


	// - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
	public function doesKeyExist($key)
	{
		if (isset($_COOKIE[$key])){
			return true;
		}

		return false;
	}
	// - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -


	// - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
	public static function delete($key)
	{
		setcookie($key, 'content', 1);
	}
	// - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -


	// - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
	public function deleteAll()
	{
		if (isset($_SERVER['HTTP_COOKIE'])) {
			$cookies = explode(';', $_SERVER['HTTP_COOKIE']);
			foreach($cookies as $cookie) {
				$parts = explode('=', $cookie);
				$name = trim($parts[0]);
				setcookie($name, '', time()-1000);
				setcookie($name, '', time()-1000, '/');
			}
		}
	}
	// - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
}